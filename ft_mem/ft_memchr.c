#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned	char		*str;
	unsigned	int			i;

	i = 0;
	str = (unsigned char*)s;
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return (str + i);
		i++;
	}
	return (NULL);
}
